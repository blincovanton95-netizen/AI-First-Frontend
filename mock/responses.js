/**
 * Подбирает ответ на основе анализа сообщения
 * @param {string} message - Сообщение пользователя
 * @param {Array} history - История диалога (для контекста)
 * @returns {string} Ответ ассистента
 */
export function getMockResponse(message, history = []) {
  const lowerMessage = message.toLowerCase();
  
  // Определяем тему по ключевым словам
  let detectedTopic = 'fallback';
  let maxScore = 0;
  
  for (const [topic, keywords] of Object.entries(KEYWORDS)) {
    const score = keywords.reduce((acc, keyword) => {
      return acc + (lowerMessage.includes(keyword) ? 1 : 0);
    }, 0);
    
    if (score > maxScore) {
      maxScore = score;
      detectedTopic = topic;
    }
  }
  
  // Если есть история и последнее сообщение было вопросом — учитываем контекст
  if (history.length > 0 && detectedTopic === 'fallback') {
    const lastUserMessage = [...history].reverse().find(m => m.role === 'user');
    if (lastUserMessage) {
      const lastLower = lastUserMessage.content.toLowerCase();
      for (const [topic, keywords] of Object.entries(KEYWORDS)) {
        if (keywords.some(k => lastLower.includes(k))) {
          detectedTopic = topic;
          break;
        }
      }
    }
  }
  
  // Выбираем случайный ответ из категории (для разнообразия)
  const responses = RESPONSES[detectedTopic];
  const randomIndex = Math.floor(Math.random() * responses.length);
  
  return responses[randomIndex];
}